    def info(self, msg: str):
        print(f"{_DIM}  {msg}{_RESET}", flush=True)

    def warn(self, msg: str):
        print(f"{_YELLOW}  ‚ö†  {msg}{_RESET}", flush=True)

    def wake(self):
        print(f"\n{_BOLD}{_GREEN}  ‚óČ  Wake word detected ‚ÄĒ listening‚Ä¶{_RESET}", flush=True)

    def user_said(self, text: str):
        print(f"\n{_BOLD}{_BLUE}  You ‚Ė∂{_RESET}  {text}\n", flush=True)

    def interrupt(self):
        print(f"\n{_BOLD}{_YELLOW}  ‚Ü©  Interrupting ‚ÄĒ listening for new command‚Ä¶{_RESET}", flush=True)

    def cancelled(self):
        print(f"\n{_DIM}  [generation cancelled]{_RESET}", flush=True)

    def agent_start(self):
        print(f"\n{_BOLD}{_MAG}  Agent ‚Ė∂{_RESET}  ", end="", flush=True)

    def token(self, tok: str):
        """Print a single streaming token ‚ÄĒ no newline."""
        print(tok, end="", flush=True)

    def line(self, text: str):
        """Print a full line (for subprocess backends)."""
        print(f"  {text}", flush=True)

